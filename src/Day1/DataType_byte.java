package Day1;


public class DataType_byte {

	public static void main(String[] args) {
		
		//byte takes 1byte
		byte byteMax=127;
		byte byteMin=-128;
		
		System.out.println("Minimum Range of byte :"+byteMin);
		System.out.println("Maximum Range of byte : "+byteMax);
		
		//short takes 2byte
		short shortMax=32767;
		short shortMin=-32768;
		
		System.out.println("\nMinimum range of short : "+shortMin);
		System.out.println("Maximum range of short : "+shortMax);
		
		//int--> 4byte
		int minint=-2147483648;
		int maxint=2147483647;
		System.out.println("\nMinimum range of int : "+minint);
		System.out.println("Maximum range of int : "+maxint);
		
		//long--> 8bytes
		//long minlong=;
		//long maxlong=;
		

	}

}

